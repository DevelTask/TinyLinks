<?php

namespace app\services;

/**
 * Интерфейс для поставщика уникальных коротких кодов.
 *
 * Отвечает за генерацию кода, гарантируя его уникальность (например, через проверку в базе данных).
 * Используется как более высокоуровневая абстракция над генератором кода.
 */
interface ShortCodeProviderInterface
{
    /**
     * Возвращает уникальный короткий код, который ещё не используется в системе.
     *
     * @return string Уникальный короткий код
     */
    public function getUniqueShortCode(): string;
}
